    private void Bt_Pesquisa_Historico(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Bt_Pesquisa_Historico
    String cpf = jTextField1.getText().trim();

    if (cpf.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this,
                "Informe o CPF do paciente para a consulta.",
                "Erro",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    List<Consulta> historico = new ConsultaDAO().buscarHistoricoPorCpf(cpf);

    if (historico.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Nenhum histórico encontrado para o CPF informado.",
            "Aviso",
            javax.swing.JOptionPane.INFORMATION_MESSAGE);
       }

       // Preenche a jTable1
javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) JTable_Historico_Consultas.getModel();
modelo.setRowCount(0); // limpa tabela

for (Consulta consulta : historico) {
    modelo.addRow(new Object[]{
        consulta.getPaciente().getCpf(),
        consulta.getPaciente().getNome(),
        consulta.getHora(),
        consulta.getData(),
        consulta.getMedico().getNome()
    });
}
    }//GEN-LAST:event_Bt_Pesquisa_Historico


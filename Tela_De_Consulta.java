    private void jButton_PrognosticoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_PrognosticoActionPerformed

    DefaultListModel<String> modeloDiagnostico = new DefaultListModel<>();
    DefaultListModel<String> modeloPrescricao = new DefaultListModel<>();

    int linhaSelecionada = JTable_Historico_Consultas.getSelectedRow();

    if (linhaSelecionada != -1) {
        String cpf = JTable_Historico_Consultas.getValueAt(linhaSelecionada, 0).toString();
        String data = JTable_Historico_Consultas.getValueAt(linhaSelecionada, 3).toString();
        String hora = JTable_Historico_Consultas.getValueAt(linhaSelecionada, 2).toString();

        try {
            Connection conexao = com.mycompany.projeto_poo2.dao.Conexao.getConection();
            String sql = "SELECT diagnostico, prescricoes FROM consulta WHERE cpf = ? AND data = ? AND hora = ?";
            PreparedStatement ps = conexao.prepareStatement(sql);
            ps.setString(1, cpf);
            ps.setString(2, data);
            ps.setString(3, hora);

            ResultSet rs = ps.executeQuery();

            modeloDiagnostico.clear();
            modeloPrescricao.clear();

            if (rs.next()) {
                String diagnostico = rs.getString("diagnostico");
                String prescricao = rs.getString("prescricoes");

                modeloDiagnostico.addElement(
                    (diagnostico != null && !diagnostico.isEmpty()) ? diagnostico : "Nenhum diagnóstico cadastrado."
                );

                modeloPrescricao.addElement(
                    (prescricao != null && !prescricao.isEmpty()) ? prescricao : "Nenhuma prescrição cadastrada."
                );

            } else {
                JOptionPane.showMessageDialog(this, "Nenhum prognóstico encontrado para essa consulta.");
                modeloDiagnostico.addElement("Nenhum diagnóstico encontrado.");
                modeloPrescricao.addElement("Nenhuma prescrição encontrada.");
            }

            jList_Diagnostico.setModel(modeloDiagnostico);
            jList_Prescriao.setModel(modeloPrescricao);

            rs.close();
            ps.close();
            conexao.close();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao buscar prognóstico: " + ex.getMessage());
            ex.printStackTrace();
        }

    } else {
        JOptionPane.showMessageDialog(this, "Selecione uma consulta na tabela primeiro.");
    }
    }//GEN-LAST:event_jButton_PrognosticoActionPerformed

